package operator;

public class Main06 {
	public static void main(String[] args) {
		int num1 = 123;
		int num2 = 456;
		
		boolean reslut1 = num1 == num2;	// false
		boolean reslut2 = num1 != num2;	// true
		boolean reslut3 = num1 > num2;	// false
		boolean reslut4 = num1 <= num2;	// true
		
		System.out.println("reslut1 : " + reslut1);
		System.out.println("reslut2 : " + reslut2);
		
		System.out.println("reslut3 : " + reslut3);
		System.out.println("reslut4 : " + reslut4);
	}

}
